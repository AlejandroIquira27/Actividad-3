    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        double a, b, c, r1, r2;
        
        try{
            String datoa = String.valueOf(txtDatoa.getText());
            String datob = String.valueOf(txtDatob.getText());
            String datoc = String.valueOf(txtDatoc.getText());
            if(datoa.isEmpty() || datob.isEmpty() || datoc.isEmpty()){
                throw new Exception();
            }
                a = Double.parseDouble(txtDatoa.getText());
                b = Double.parseDouble(txtDatob.getText());
                c = Double.parseDouble(txtDatoc.getText());
                if(a != 0){
                    r1 = Solucion.Solucion_Ecuacion(a, b, c);
                    r2 = Solucion2.Solucion_Ecuacion(a, b, c);
                    lblRes1.setText("El valor de X es: " + String.valueOf(r1));
                    lblRes2.setText("El valor de X tambien es: " + String.valueOf(r2));
                    lblMensaje.setText("");
                    lblBlanco.setText("");
                }
                else{
                    lblMensaje.setText("No se puede dividir por 0");
                }
        }
        catch(NumberFormatException nfe){
            lblMensaje.setText("Datos no validos");
            lblBlanco.setText("");
            lblRes1.setText("");
            lblRes2.setText("");
        }
        catch(Exception ex){
            lblBlanco.setText("Espacios en blanco");
            lblMensaje.setText("");
            lblRes1.setText("");
            lblRes2.setText("");
        }
       
    }//GEN-LAST:event_btnCalcularActionPerformed

